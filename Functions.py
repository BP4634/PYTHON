def sum_of_digits(a,b):
    print(a+b)

sum_of_digits(7,8)
sum_of_digits(18,9)


#Create a function that can reverse any string

def reverse_string(a):
    c = str(a)
    b = c[::-1]
    
    
    print(b)

reverse_string("abcd")
reverse_string(12345)
reverse_string("aTn7ps")









